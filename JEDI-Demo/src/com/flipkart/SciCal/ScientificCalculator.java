package com.flipkart.SciCal;

public class ScientificCalculator {
    public double sinVal(double x){
        return Math.sin(x);
    }
    public double cosVal(double a){
        return Math.cos(a);
    }
    public double sqrtVal(double a){
        return Math.sqrt(a);
    }

    public double logVal(double a){
        return Math.log(a);
    }
}
